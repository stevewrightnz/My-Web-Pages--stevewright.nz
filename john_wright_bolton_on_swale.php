	<h3>Will of John Wright of Bolton on Swale</h3>
	
	<p>Reference: ZBL I/1/1/398 <sup><a href="#fn1" id="r1">[1]</a></sup></p>
	<p>Date: 15 Mar 1748/9 </p>
	<p>Title: Copy will of John Wright of Bolton on Swale, esq.</p>
	
	<p>Freehold property in Bolton on Swale, Greenbury and Scorton, in Newcastle upon Tyne to trustees the Rev. 
	Timothy Dickinson and the Rev. John Noble of Scorton, clerks and Ralph Hutchinson of Catterick</p>
 
	<p>In trust that</p>
 
	<p>if Margaret, his wife, releases dower, then she may hold the mansion house at Bolton plus outhouses, 
	gardens, etc., and the Bolton tithes rent free till son John is 21</p>
 
	<p>And that</p>
 
	<p>when John is 21 she shall have house at Scorton for life at £7 p.a.</p>
 
	<p>And that</p>
 
	<p>they shall pay the interest on his mortgages</p>
 
	<p>And that</p>
 
	<p>they shall pay wife £30 p.a.</p>
 
	<p>And that</p>
 
	<p>younger children £200 each on reaching 21</p>
 
	<p>tuition and guardianship of children to Margaret</p>
 
	<p>part of £200 may be spent putting the younger children to business</p>


