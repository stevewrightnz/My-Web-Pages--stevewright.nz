	<h4 id="will">The last will of John Wright of Plowland<sup><a href="#fn1" id="r1">[1]</a></sup></h4>
	
	<p>In the name of God, Amen. The xvj day of Auguste, 1540, I, John Wright, of Pleughlande, 
	within the parishe of Welwicke, holl of mynde and perfite of memorie, make my will in this 
	manner followinge. First I bequeathe my saull to God Almightie, to oure blissed laidie and 
	to the celestiall company of heven, my bodie to be buried within the hallowed grounde. 
	Item I bequeathe to John Wright x li., to Elisabethe Wright xli., to Charles Wright fyve 
	marke, to John Cloughe v marc, to William Buccler xl s., to Richarde Beaniles wif xl a., 
	to every one of Richarde Smyth children, who be fyve, xl s., to Georgie Cloughe xl s., 
	to William Picherde xl s., to Margaret Hent fyve markes, to William Battirsbie v marc, 
	to Richarde Curie xiij s. iiij d., to the vicare of Welwike vis. viij d., to John Arcules 
	vj s. viij d., to William Hirdman ij s., to Thomas Carter vj s. viij d., to Rollande Murrey 
	vj s. viij d., to Helin vj s. viij d., to Agnes xl d., to Besse ij d., to Julian ij s., to 
	Georgie Thorp wif iiij quarters whet, to John Ferou's wif iiij quarters whet, to 
	Sir John Foston to singe for my saull seven yeres xl s. in the yere, to waite one my wif, 
	with mett and drinke, to the churche of Welwike vj s. viij d.,to the churche of Pattrington 
	v s., to the churche of Hollym vi s. viij d., to the churche of Holmton vj s. viij d. Item I 
	give to my wif all her plaite aiid the goodes that she brought with her. To Robert Wright my 
	sone all my plate. Also I will that Sir William Constable, knyght, Sir Rauf Ellerker, knyght, 
	maister Babthorpe, esquier, and Mr Walter Grymstone, esquier, to be supervysors of this my 
	laste will, and evere one of them to have fyve markes. The residue of all my goodes I bequeathe 
	to Robert Wright, my sone, whom I ordan my full executor. Thes being witnes and recordes, 
	the vicare of Welwike, my curate, Bartilmewe Thorp, gentleman, William Carr and 
	John Parker, yomen. </p>
	
	<p>[Proved 28 April, 1541.]	</p>
	
	<h4 id="land">Land Transactions by John Wright</h4>
	
	<p>In "1519—MICHAELMAS TERM, 11 HENRY VIII.", "Walter Grymston, Nicholas Palmes, George Wright, and Laurence Diconson, chaplain" purchased 
	from "John Wright and Alice his wife, one of the daughters and heirs of John Ryther, esq.", "Messuage<sup><a href="#fn2" id="r2">[2]</a></sup>, and 4 cottages with lands in 
	Ottryngham, Wynested, Hollym, and Raynthorp, and the fifth part of 4 messuages with lands in Penesthorp, Southorp, Kilnesey, Beuerley,
	and Holmerton."<sup><a href="#fn3" id="r3">[3]</a></sup></p>
	
	<p>In "1531—MICHEALMAS TERM, 23 HENRY VIII", "James Fox and John Wilkynson" purchased from "John Wright and 
	Alice his wife, one of the daughters and heirs of John Ryther", "5 messuages with lands in Otteryngham, Wynested, 
	Hollym, and Raynthorp, and also a fifth part, divided into five parts, of 5 messuages with lands in Penesthorp, 
	Southorpp, Kylnesey, Beverley, and Holmeton, in which William Ryther has a life interest."<sup><a href="#fn4" id="r4">[4]</a></sup></p>
	
	<p> In "1533—MICHAELMAS TERM, 25 HENRY VIII.", "John Wright" purchased from "Gerard Elwyn and Johanna his wife, George Thorpe and Elizabeth his wife", 
	"Messuage with lands in Plughland."<sup><a href="#fn5" id="r5">[5]</a></sup></p>
		
