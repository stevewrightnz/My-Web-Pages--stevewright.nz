	<h4 id="other">Extract from The Gunpowder Plot and Lord Mounteagle's Letter<sup><a href="#fn9" id="r9">[9]</a></sup></h4>
	
	<p>The Estate of Plowland came into the Wright family in the reign of Henry VIII., owing to John 
	Wright, Esquire (a man of Kent), having married Alice Ryther, one of the co-heiresses of Sir John 
	Ryther, of Ryther, on the banks of the "lordy Wharfe", between York and Selby.</p>
	
	<p> John Wright's son, Robert, succeeded as the owner of Plowland (or Plewland).  Robert 
	Wright married for his second wife Ursula Rudston, whose family had been lords of Hayton near 
	Pocklington, from the days of King John.  Ursula Wright was akin to the Mallory (or Mallorie) 
	family, of Studley Royal, Ripon, and so a cousin in some degree to most of the grand old Yorkshire 
	gentry, such as the Ingleby family, of Ripley Castle and Harewell Hall, Darce, near Brimham Rocks,
	in Nidderdale, and the Markenfields, of Markenfields Hall, near Ripon, to mention none others beside.</p>
	
	<p>Robert Wright (the second Wright who owned Plowland) had been married before his marriage to 
	Ursula Rudston.  His first wife's name was Anne Grimstone. She was the daughter of Thomas Grimstone, 
	Esquire, of Grimstone Garth.  Robert Wright and Anne Grimstone had one son who "heired" Plowland.  
	His name was William Wright. He married Ann Thornton, of East Newton, in Rydale, a lady who was 
	related to many	old Rydale and Vale of Mowbray families in the North Riding of Yorkshire.  The names
	of William Wright and Ann, his wife (born Thornton), are still recorded on a brass in the north isle 
	of Welwick Church.</p>
	
	<p> William Wright was a half-brother to Ursula Ward, the wife of Marmaduke Ward, of Mulwith, Newby, 
	and Givendale, near Ripon, the parents of the great Mary Ward, the friend of popes, emperors, kings, 
	nobles, statesmen, warriors and indeed of the most distinguished personages of Europe during the reigns 
	of James I. and Charles I.  William Wright (or Wryght, as the name is spelt on the brass in Welwick Church) 
	was also half-brother to the two Gunpowder conspirators, John and Christopher Wright, who were slain at 
	Holbeach House, Staffordshire, a few days after the capture of Guy Fawkes by Sir Thomas Knevet, early 
	in the morning of November 5th, 1605.</p>
	<p>
	</p>
	
	<h4>Wright Brothers of Plowland and The Gunpowder Plot</h4>
	
	<p>Along with eleven others John Wright and Christopher Wright were part of the plot to assassinate King James I of England
	and VI of Scotland by blowing up the House of Lords during the State Opening of England's Parliament on 5 November 1605.  
	This act was to be the prelude to a popular revolt in the Midlands during which James's nine-year-old daughter, Princess 
	Elizabeth, was to be installed as the Catholic head of state.
	</p>

	<h5>John Wright<sup><a href="#fn10" id="r10">[10]</a></sup></h5>

	<p>Mr John Wright was a gentleman of Yorkshire, not born to any great fortune, but lived always in place and
	company of the better sort.   In his youth and for the most of his time very wild and disposed to fighting and trial 
	of his manhood, as I touched before. He became Catholic about the time of my Lord of Essex his attempt, in which 
	he was ; and after that time kept much with Mr. Catesby and some other gentlemen of his friends and acquaintance. 
	He grew to be staid and of good sober carriage after he was Catholic, and kept house in Lincolnshire, where he 
	had Priests come often, both for his spiritual comfort and their own in corporal helps. He was about forty years old, 
	a strong and a stout man, and of a very good wit, though slow of speech ; much loved by Mr. Catesby for his valour 
	and secrecy in carriage of any business, which, I suppose, was the cause why he was one of the first acquainted with 
	this unfortunate enterprise. </p>
	
	<h5>Christopher Wright<sup><a href="#fn11" id="r11">[11]</a></sup></h5>
	<p> Mr. Christopher Wright, younger brother to John Wright ......., by whom also this other may be known without 
	new description. For though he were not like him in face, as being fatter and a lighter coloured hair and taller of 
	person, yet was he very like to the other in conditions and qualities, and both esteemed and tried to be as stout a man 
	as England had and withal a zealous Catholic and trusty and secret in any business as could be wished : in respect 
	whereof they esteemed him very fit to be of their company and so caused him to take the oath of secrecy and he 
	received the Blessed Sacrament thereupon (as they had also- done) and so admitted him. Not long after they admitted 
	also another, which was Mr. Robert Winter, the eldest brother to Thomas Winter before spoken of. </p>
	
