	<h5>Inscription	<sup><a href="#fn14" id="r14">[14]</a></sup></h5>
	<p>"Here lie interred ye bodies of William Wright, of Plowland, Esq. and Ann, his wife, who, after they had lived lovingly together ye space of fifty yeares 
	in the fear of God and love of men, finished a faire pilgrimage to a joyfull Paradice, Ann, ye 28 of Dec. in the year of grace 1618, and ye said Willm.
	ye 23 August , A. Dni. 1621, whose sowles God hath in His blessed keeping.  Memoria justi vivet in Eternum"</p>

	
