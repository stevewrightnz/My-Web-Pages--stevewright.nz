		<h3>Wright Family Genealogy</h3>

		<p>Wright is an occupational surname originating in England. The term 'Wright' comes from the Old English word 'wryhta' or 'wyrhta' (circa 700 AD), 
		meaning worker or shaper of wood. It later became any occupational worker (for example, a shipwright is a person who builds ships), and is used 
		as a British family name. Wright is also an anglicized version of the Scots Gaelic clan name "MacIntyre" or "Mac an t-Saoir", meaning "son of the wright", 
		or "son of the carpenter".<sup><a href="#fn1" id="r1">[1]</a></sup></p>

		<p>The root person for the research documented in these pages is Nicholas "Nicko" Wright and the information attempts to capture both his parents families.</p>

		<p>The initial research on the Wright side of the family was carried out by Nickos' grandaunt.  The detail on his mothers side of the family comes mostly from 
		information provided by his maternal grandmother. The remaining research and some filling in of gaps has been carried out over a couple of years and is generally 
		based on freely available internet records (I'm far too tight to pay for stuff if I don't have to).</p>

		<p>We have managed to tentatively trace our bit of the Wright family back to John Wright of Plowland in Holderness, Yorkshire.
		There are some references to John originating in Kent and settling at Holderness in about about 1542.
		"John Wright, of Ploughland Hall, Seneschall to Henry VIII, came out of Kent 33 Henry VIII"<sup><a href="#fn2" id="r2">[2]</a></sup>. </p> 
		<p>It is probable that either the date, or reference to "33 Henry VIII" is incorrect as the thirty third year of the reign of King Henry VIII is between 
		22 April 1541 - 21 April 1542 which, is some two years after his death. </p>

		<p>John also "wedded at the close of the fourteenth, or in the beginning of the fifteenth century, Alice, daughter and co-heir
		of John Ryther, son and heir of Gilbert Ryther, who died seized of Alford Well and Sutton, and grandson of Sir William Ryther,
		Knt."<sup><a href="#fn3" id="r3">[3]</a></sup></p>

		<p>John and his wife Alice were <a href='john_wright_plowland.php#land' target=_blank>trading land</a> in Yorkshire in October - December 1519 (22 Henry VIII). 
		We know John died sometime between 6 August 1540 and 28 April 1541 as his <a href="john_wright_plowland.php#will" target=_blank>will</a> is dated 
		6 August 1540 and was proved on 28 April 1541."<sup><a href="#fn4" id="r4">[4]</a></sup></p>
		
		<p>Further information on John Wright of Plowland Hall and his descendants can be found <a href="john_wright_plowland.php#other" target=_blank>here</a>.</p>

		<h2>The Birchville, New Zealand branch of the Wright Family</h2>

		<p>Back in 1962 or 1963 Nicko's grandparents uprooted the family from the West Midlands in England and emigrated to Wellington, New Zealand with the 
		intention of staying for a couple of years.</p>

		<p>The first stop was 5 Cornwall Street in <a href="https://en.wikipedia.org/wiki/Lower_Hutt" target=_blank> Lower Hutt</a>.  
		The second was <a href="https://en.wikipedia.org/wiki/Birchville" target=_blank> Birchville</a>, 
		just north of <a href="https://en.wikipedia.org/wiki/Upper_Hutt" target=_blank> Upper Hutt</a>, where they still live.</p>

		<p>Their children attended Te Marua/Brown Owl School followed by Maidstone Intermediate, Upper Hutt College and various tertiary institutions 
		in the lower North Island.  The family remains based in the lower North Island of New Zealand.</p>

		<p>Only the direct lines are published here, as that is all the RootsMagic software I'm using will allow at the moment. Other branches of the family that we 
		have documented link to some interesting historical figures.</p>
		
		<p>Where you think that you can provide missing information please don't hesitate to contact us as we are always keen to discover more about our families.  We
		will also try and help out with your research where we can, just ask!</p>
